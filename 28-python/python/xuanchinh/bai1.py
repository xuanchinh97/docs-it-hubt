# Viết chương trình yêu cầu người dùng nhập vào 2 số nguyên, in ra màn hình tổng, hiệu, tích, thương, phần nguyên và phần dư của 2 số đó ?

a = int(input("nhập vào số a: "))
b = int(input("nhập vào số b: "))

print("tổng hai số  a và b: " , a +b)
print("hiệu hai số  a và b: " ,a -b)
print("tích hai số  a và b: " , a *b)
print("thương hai số  a và b: " , a %b)
print("dư hai số  a và b: " , a / b)
